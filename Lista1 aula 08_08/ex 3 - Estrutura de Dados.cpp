#include <stdio.h>  // Inclui a biblioteca padrão de entrada e saída

int main()  // Função principal do programa
{
    int n, i;  // Declaração das variáveis inteiras 'n' e 'i'

    // Lendo o valor de 'n'
    printf("Digite um número inteiro positivo: ");  // Exibe uma mensagem solicitando ao usuário que digite um número
    scanf("%d", &n);  // Lê o número digitado pelo usuário e armazena na variável 'n'

    // Exibindo os 'n' primeiros números pares
    printf("Os %d primeiros números pares são:\n", n);  // Exibe uma mensagem indicando que serão mostrados os primeiros 'n' números pares
    
    for (i = 1; i <= n; i++)  // Loop que vai de 1 até 'n'
    {
        printf("%d ", 2 * i);  // Calcula e imprime o enésimo número par (2 * i)
    }

    printf("\n");  // Move o cursor para a próxima linha após imprimir os números pares

    // Exibindo os 'n' primeiros números ímpares
    printf("Os %d primeiros números ímpares são:\n", n);  // Exibe uma mensagem indicando que serão mostrados os primeiros 'n' números ímpares
    
    for (i = 1; i <= n; i++)  // Loop que vai de 1 até 'n'
    {
        printf("%d ", 2 * i - 1);  // Calcula e imprime o enésimo número ímpar (2 * i - 1)
    }

    printf("\n");  // Move o cursor para a próxima linha após imprimir os números ímpares

    return 0;  // Retorna 0 para indicar que o programa terminou com sucesso
}
